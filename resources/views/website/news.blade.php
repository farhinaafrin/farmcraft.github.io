                                        <h3>HAJARI GUR</h3>
                                        <p> HAJARI GUR” (molasses) has a very unique tradition and heritage at
                                            Shikderpara village in
                                            Harirampur upazila of Manikganj in BANGLADESH. Locals say English Queen
                                            Elizabeth had
                                            given the name, 'Hajari'.
                                            With its uniqueness and tradition, which can be traced back to 200 years,
                                            Hajari molasses are
                                            internationally renowned .</p>
                                        <blockquote>
                                            <p><b>History of Hajari Gur : </b>
                                                According to locals, about two hundred years ago, a man by the name of
                                                Mohammad Hazari started
                                                making this special variety of molasses from date juice in the Jhitka
                                                area of Harirampur upazila of
                                                Manikganj. In fact, Hazari molasses is named after him, and the date
                                                molasses harvesters (called
                                                gachhis) of the Jhitka area have kept the 200-year-old tradition alive.
                                                It is also said that during her visit to Dhaka, Queen Elizabeth II of
                                                England praised Hajari Gur.
                                                Hajari molasses is priced in the local market at around Tk1,000-1,500,
                                                while other types of
                                                molasses are sold for Tk250-450 per kg.
                                            </p>
                                            <p class="mb-0"><cite>Farmcraft</cite></p>
                                        </blockquote>

                                        <p><b>Why is it unique?</b> Hajari molasses is made from the juice collected for
                                            three days after the date palm trees are cut.
                                            The tree is prepared again and dried in the sun. After three days, the juice
                                            is collected again for an
                                            additional three-day period.
                                            Anwar Hossain, a gachhi (tree climber) said, "To make Hajari Gur, we collect
                                            date juice for three
                                            days in a row. Then, we don't collect juice from those trees for the next
                                            two days. On the third day,
                                            we again prepared the trees to collect juice."
                                            The quality and taste of molasses made from date juice collected every three
                                            days will not be the
                                            same as other kinds of molasses, he added.
                                            One kilogram of Hajari Gur requires 12-13 kg of juice from date palm trees.
                                            This specific variety
                                            of molasses is made at the culmination of various processes after juice is
                                            collected from the tree
                                            every morning.
                                            Hajari molasses manufacturer Shahid Hajari said, "The number of date palms
                                            is decreasing by the
                                            day. To add to that, we also are facing a huge fuel crisis. Therefore, we're
                                            not being able to cater
                                            to the demand", he said.
                                            Abul Kashem Hajari, another manufacturer, said due to the high demand for
                                            Hajari Gur, some
                                            unscrupulous traders have been producing sub-standard molasses and selling
                                            it for cheap, passing
                                            it off as genuine Hajari Gur.
                                            Shafiqul Islam Hajari Shamim, proprietor of Hajari Products Manikganj, said
                                            20 to 25 families in
                                            the Jhitka area produce 50 to 60 kg of molasses every day. Initiatives have
                                            been taken to market
                                            genuine molasses and preventing adulteration by pasting the registered
                                            trademark on their packets.
                                            They urgently seek government support to boost molasses production and to
                                            preserve local
                                            heritage.
                                            Manikganj Deputy Commissioner SM Ferdous said the very name of Hajari
                                            molasses is associated
                                            with the tradition of the Manikganj district, and the administration has
                                            always sought to preserve
                                            its local heritage.
                                        </p>

