                                        <h3>Dairy product</h3>
                                        <p> Dairy is one of Australia’s most important rural industries, producing about
                                            8.8 billion litres of
                                            milk in 2018-19 and directly employing approximately 46,200 people. It is
                                            the fourth-largest rural
                                            industry in Australia generating $4.4 billion in farm gate value in 2018-19.
                                            The majority of milk
                                            production occurs on the south-east seaboard in Victoria, New South Wales,
                                            and Tasmania.
                                            Australia exports approximately 35 per cent of its milk production, with
                                            exports valued at $3.2
                                            billion in 2018-19. A large proportion of exports are in the form of
                                            value-added products such as
                                            cheese, butter, ultra-heat treated milk and milk powders. Australian dairy
                                            is exported around the
                                            world.
                                        </p>
