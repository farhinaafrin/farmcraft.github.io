                                        <h3>Avocado</h3>
                                        <p> Avocados or “alligator pears” are known for their creamy smooth flesh and
                                            bumpy skin. They are
                                            a popular food across many cultures. Perhaps best known as the star
                                            ingredient in guacamole, they
                                            are versatile and prepared in an array of dishes, or simply eaten plain with
                                            a spoon.
                                            .</p>
                                        <blockquote>
                                            <p><b>Although</b>
                                                not
                                                sweet, avocados are botanically classified as a fruit with a large berry
                                                and single center pit, grown
                                                from the Malaysian tree.
                                            </p>
                                            <p class="mb-0"><cite>Farmcraft</cite></p>
                                        </blockquote>

                                        <p><b>Their</b> nutrition profile makes them a staple in various healthful meal
                                            plans. Avocados are a good
                                            source of fiber, and contain more fat (the good kind) than carbohydrate, so
                                            are popular on lowercarbohydrate diets such as with diabetes. Their
                                            heart-friendly fats do not increase blood
                                            cholesterol, which can provide satisfaction on a traditional
                                            cholesterol-lowering diet that is often
                                            low in fat and cholesterol. It is one the highest-fat plant foods, making it
                                            a popular inclusion in
                                            vegan and vegetarian diets. The slightly earthy but neutral flavor of
                                            avocados works well in sauces,
                                            salad dressings, sandwiches, baked goods, salads, and grain dishes to add
                                            richness.
                                        </p>

