                                        </blockquote>

                                        <p><b>The</b> most popular dairy breed in Australia is the Holstein/Friesian.
                                            These black and white cows
                                            which originate from the Netherlands via America are known for their high
                                            milk production, large
                                            size and docile temperament. These are also the most popular breed
                                            world-wide, so farmers have
                                            the biggest choice of sires (males) for their cows.
                                            The second most popular breed in Australia are Jerseys. Originating from the
                                            English Channel
                                            Island Jersey, these cows are smaller in stature and caramel-coloured,
                                            producing a smaller volume
                                            of milk which is rich in butterfat and protein. These cows are popular,
                                            especially in grass grazing
                                            farms, because of their hardiness, size, fertility and again their high
                                            butterfat and protein in the in
